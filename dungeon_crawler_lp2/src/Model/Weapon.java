public class Weapon extends Artefact {
    private int damage;
    
    public Weapon(int damage){
        this.damage = damage;
    }

    /**
     * @return the damage
     */
    public int getDamage() {
        return damage;
    }

    /**
     * @param damage the damage to set
     */
    public void setDamage(int damage) {
        this.damage = damage;
    }
    
}
